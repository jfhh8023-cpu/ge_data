/* ========== 主函数 ========== */
async function main() {
  try {
    await sequelize.authenticate();
    console.log('[DB] 连接成功');

    // 1. 清空所有数据（按外键顺序）
    console.log('[1] 清空所有数据...');
    await LinkPermission.destroy({ where: {}, force: true });
    await AccessLink.destroy({ where: {}, force: true });
    await WorkRecord.destroy({ where: {}, force: true });
    await FillLink.destroy({ where: {}, force: true });
    await MatchGroup.destroy({ where: {}, force: true });
    await StaffFillLink.destroy({ where: {}, force: true });
    await CollectionTask.destroy({ where: {}, force: true });
    await Staff.destroy({ where: {}, force: true });
    console.log('  ✓ 全部清空');

    // 2. 创建人员
    console.log('[2] 创建人员...');
    const staffMap = {};
    for (const s of STAFF_LIST) {
      const staffId = uuidv4();
      const staff = await Staff.create({ id: staffId, name: s.name, role: s.role });
      staffMap[s.name] = staff;
      // 同时创建 StaffFillLink
      const token = `${staffId.substring(0, 8)}_${uuidv4().replace(/-/g, '').substring(0, 12)}`;
      await StaffFillLink.create({ id: uuidv4(), staff_id: staffId, token });
      console.log(`  ✓ ${s.name} (${s.role})`);
    }

    // 3. 创建周任务（1-15周，跳过6/7/8周因为数据中没有）
    console.log('[3] 创建周任务...');
    const weekNums = [...new Set(RAW_DATA.map(r => r[0]))].sort((a, b) => a - b);
    const taskMap = {};
    for (const wn of weekNums) {
      const dates = getWeekDates(2026, wn);
      const task = await CollectionTask.create({
        id: uuidv4(),
        title: `语音业务线-2026年第${wn}周工时统计`,
        time_dimension: 'week',
        week_number: wn,
        year: 2026,
        start_date: dates.start,
        end_date: dates.end,
        status: wn < 15 ? 'closed' : 'active',
        is_preferred: wn === 15
      });
      taskMap[wn] = task;
      console.log(`  ✓ 第${wn}周: ${dates.start} ~ ${dates.end}`);
    }

    // 4. 导入工时记录 + MatchGroup
    console.log('[4] 导入工时记录...');
    let totalRecords = 0;
    let totalMatchGroups = 0;

    for (const row of RAW_DATA) {
      const [weekNum, version, reqTitle, pm, beText, feText, testText] = row;
      const task = taskMap[weekNum];
      if (!task) continue;

      const pmArray = pm ? [pm] : [];
      const versionStr = (!version || version === '-') ? null : version;

      // 解析三个工种
      const beParsed = parsePersonHours(beText);
      const feParsed = parsePersonHours(feText);
      const testParsed = parsePersonHours(testText);

      // 合并所有备注
      const allRemarks = [...beParsed.remarks, ...feParsed.remarks, ...testParsed.remarks];

      // 创建 WorkRecord
      const allRecords = [...beParsed.records, ...feParsed.records, ...testParsed.records];
      for (const rec of allRecords) {
        const staffName = NAME_ALIAS[rec.name] || rec.name;
        const staff = staffMap[staffName];
        if (!staff) {
          console.warn(`  ⚠ 未找到人员: ${staffName} (需求: ${reqTitle})`);
          continue;
        }
        await WorkRecord.create({
          id: uuidv4(),
          task_id: task.id,
          staff_id: staff.id,
          requirement_title: reqTitle,
          version: versionStr,
          product_managers: JSON.stringify(pmArray),
          hours: rec.hours
        });
        totalRecords++;
      }

      // 创建 MatchGroup（包含所有字段 + 备注）
      const feJson = feParsed.records.map(r => ({ name: r.name, hours: r.hours }));
      const beJson = beParsed.records.map(r => ({ name: r.name, hours: r.hours }));
      const testJson = testParsed.records.map(r => ({ name: r.name, hours: r.hours }));
      // 过滤备注：去掉人名部分，只保留真正的状态文字
      const filteredRemarks = testParsed.remarks.filter(r => {
        const t = r.trim();
        if (!t || t === '-' || t === '--') return false;
        // 去掉人名片段
        const resolvedName = NAME_ALIAS[t] || t;
        if (KNOWN_NAMES.has(resolvedName)) return false;
        // 去掉 "人名/-" 形式产生的残余
        if (/^[\s\-]+$/.test(t)) return false;
        return true;
      });
      const remarkStr = filteredRemarks.length > 0 ? ('测试: ' + filteredRemarks.join('; ')) : null;

      await MatchGroup.create({
        id: uuidv4(),
        task_id: task.id,
        merged_title: reqTitle,
        version: versionStr,
        product_managers: JSON.stringify(pmArray),
        frontend: JSON.stringify(feJson),
        backend: JSON.stringify(beJson),
        test_role: JSON.stringify(testJson),
        remark: remarkStr,
        confidence: 1.00,
        status: 'auto_merged'
      });
      totalMatchGroups++;
    }

    console.log(`  ✓ 共导入 ${totalRecords} 条工时记录`);
    console.log(`  ✓ 共创建 ${totalMatchGroups} 条 MatchGroup`);
    console.log('\n[完成] 数据导入成功！');

    // 5. 统计概览
    console.log('\n========== 导入统计 ==========');
    console.log(`人员数: ${STAFF_LIST.length}`);
    console.log(`周任务数: ${weekNums.length} (${weekNums.join(', ')})`);
    console.log(`工时记录数: ${totalRecords}`);
    console.log(`MatchGroup数: ${totalMatchGroups}`);

    // 按周统计
    const weekRecordCount = {};
    for (const row of RAW_DATA) {
      weekRecordCount[row[0]] = (weekRecordCount[row[0]] || 0) + 1;
    }
    for (const wn of weekNums) {
      console.log(`  第${wn}周: ${weekRecordCount[wn]} 条需求`);
    }

  } catch (err) {
    console.error('[错误]', err);
    process.exitCode = 1;
  } finally {
    await sequelize.close();
  }
}

main();
