/* ========== 周任务定义（第1-15周, 2026年） ========== */
function getWeekDates(year, weekNum) {
  const jan4 = new Date(year, 0, 4);
  const dayOfWeek = jan4.getDay() || 7;
  const firstMonday = new Date(jan4);
  firstMonday.setDate(jan4.getDate() - dayOfWeek + 1);
  const start = new Date(firstMonday);
  start.setDate(firstMonday.getDate() + (weekNum - 1) * 7);
  const end = new Date(start);
  end.setDate(start.getDate() + 6);
  return {
    start: start.toISOString().slice(0, 10),
    end: end.toISOString().slice(0, 10)
  };
}

/**
 * 判断一个字符串是否为"非人名的纯状态文字"
 * 即：不是已知人名，且不包含任何数字
 */
function isRemarkOnly(text) {
  if (!text || text === '-' || text === '--') return false;
  const t = text.trim();
  if (KNOWN_NAMES.has(t)) return false;
  // 如果完全没有数字，且不是已知人名，就是备注
  if (!/\d/.test(t)) return true;
  return false;
}

/**
 * 解析人名/工时列
 * 输入格式示例：
 *   "邬涛/40"
 *   "刘君/32, 温嘉敦/16"
 *   "艾婉/进行中/16, 朱俊锋/6"
 *   "朱俊锋/艾婉/开发中"   （多人名+状态，无工时）
 *   "测试无需介入"          （纯备注）
 *   "朱俊锋/已提测未开始"   （人名+纯状态，无工时数字）
 *   "-"                    （无数据）
 *
 * 返回: { records: [{name, hours}], remarks: [string] }
 */
function parsePersonHours(cellText) {
  const result = { records: [], remarks: [] };
  if (!cellText || cellText === '-' || cellText === '--') return result;

  const text = cellText.trim();

  // 整格为纯状态文字（无人名、无数字）
  if (isRemarkOnly(text)) {
    result.remarks.push(text);
    return result;
  }

  // 按 ", " 或 "," 分隔多人
  const parts = text.split(/,\s*/);

  for (const part of parts) {
    const segments = part.split('/').map(s => s.trim()).filter(Boolean);
    if (segments.length === 0) continue;

    // 尝试识别每个 segment
    let name = null;
    let hours = null;
    const partRemarks = [];

    for (const seg of segments) {
      const normalized = NAME_ALIAS[seg] || seg;

      if (KNOWN_NAMES.has(normalized) || KNOWN_NAMES.has(NAME_ALIAS[normalized])) {
        // 如果已经有一个 name 且还没有 hours，先把前面的 name 存起来（无工时）
        if (name && hours === null) {
          // 多人名在同一个 part 中，如 "朱俊锋/艾婉/开发中"
          // 前面的 name 没有工时，跳过不录入 WorkRecord
        }
        name = NAME_ALIAS[normalized] || normalized;
      } else if (/\d/.test(seg)) {
        // 含数字的 segment，提取数字作为工时
        const num = parseFloat(seg.replace(/[^\d.]/g, ''));
        if (!isNaN(num) && num >= 0) {
          hours = num;
        }
        // 如果含有非数字文字，也提取为备注
        const nonNum = seg.replace(/[\d.]/g, '').trim();
        if (nonNum) {
          partRemarks.push(nonNum);
        }
      } else if (seg !== '-' && seg !== '--') {
        // 纯文字 segment，不是人名也不含数字也不是短横线 → 状态/备注
        partRemarks.push(seg);
      }
    }

    // 录入记录
    if (name && hours !== null && hours > 0) {
      result.records.push({ name, hours });
    } else if (name && hours === 0) {
      result.records.push({ name, hours: 0 });
    }

    // 收集备注
    if (partRemarks.length > 0) {
      result.remarks.push(...partRemarks);
    }
  }

  return result;
}

