/* ========== 主函数 ========== */
async function main() {
  try {
    await sequelize.authenticate();
    console.log('[DB] 连接成功');

    // 1. 清空所有数据（按外键顺序）
    console.log('[1] 清空所有数据...');
    await LinkPermission.destroy({ where: {}, force: true });
    await AccessLink.destroy({ where: {}, force: true });
    await WorkRecord.destroy({ where: {}, force: true });
    await FillLink.destroy({ where: {}, force: true });
    await MatchGroup.destroy({ where: {}, force: true });
    await CollectionTask.destroy({ where: {}, force: true });
    await Staff.destroy({ where: {}, force: true });
    console.log('  ✓ 全部清空');

    // 2. 创建人员
    console.log('[2] 创建人员...');
    const staffMap = {};
    for (const s of STAFF_LIST) {
      const staff = await Staff.create({ id: uuidv4(), name: s.name, role: s.role });
      staffMap[s.name] = staff;
      console.log(`  ✓ ${s.name} (${s.role})`);
    }

    // 3. 创建周任务（1-15周，跳过6/7/8周因为数据中没有）
    console.log('[3] 创建周任务...');
    const weekNums = [...new Set(RAW_DATA.map(r => r[0]))].sort((a, b) => a - b);
    const taskMap = {};
    for (const wn of weekNums) {
      const dates = getWeekDates(2026, wn);
      const task = await CollectionTask.create({
        id: uuidv4(),
        title: `语音业务线-2026年第${wn}周工时统计`,
        time_dimension: 'week',
        week_number: wn,
        year: 2026,
        start_date: dates.start,
        end_date: dates.end,
        status: wn < 15 ? 'closed' : 'active'
      });
      taskMap[wn] = task;
      console.log(`  ✓ 第${wn}周: ${dates.start} ~ ${dates.end}`);
    }

    // 4. 导入工时记录
    console.log('[4] 导入工时记录...');
    let totalRecords = 0;

    for (const row of RAW_DATA) {
      const [weekNum, version, reqTitle, pm, beNames, beHours, feNames, feHours, testNames, testHours] = row;
      const task = taskMap[weekNum];
      if (!task) continue;

      const pmArray = pm ? [pm] : [];

      // 解析多人记录
      function parseMulti(names, hours) {
        if (!names || names === '') return [];
        const nameArr = names.split('/');
        const hourArr = hours ? hours.split('/') : [];
        return nameArr.map((n, i) => ({
          name: n.trim(),
          hours: parseFloat(hourArr[i]) || 0
        })).filter(r => r.name && staffMap[r.name]);
      }

      const beRecords = parseMulti(beNames, beHours);
      const feRecords = parseMulti(feNames, feHours);
      const testRecords = parseMulti(testNames, testHours);

      const allRecords = [...beRecords, ...feRecords, ...testRecords];

      for (const rec of allRecords) {
        const staff = staffMap[rec.name];
        if (!staff || rec.hours <= 0) continue;
        await WorkRecord.create({
          id: uuidv4(),
          task_id: task.id,
          staff_id: staff.id,
          requirement_title: reqTitle,
          version: version || null,
          product_managers: JSON.stringify(pmArray),
          hours: rec.hours,
          status: 'submitted'
        });
        totalRecords++;
      }
    }

    console.log(`  ✓ 共导入 ${totalRecords} 条工时记录`);
    console.log('\n[完成] 数据导入成功！');

  } catch (err) {
    console.error('[错误]', err);
  } finally {
    await sequelize.close();
  }
}

main();
